// ---- パース ----
const rows = RAW.trim().split('\n').map((line) => {
  const [num, groups, assign] = line.trim().split(/\s+/);
  return { num: Number(num), groups, assign };
});

// ---- 検証 ----
const errors = [];
const seenKeys = new Set();
const table = {}; // key(sorted groups) -> assignment(slot order)

for (const { num, groups, assign } of rows) {
  const groupArr = groups.split('');
  const assignArr = assign.split('');

  // 2) 通過グループが相異なる8個 (A-L)
  const gset = new Set(groupArr);
  if (groupArr.length !== 8 || gset.size !== 8 || groupArr.some((g) => !'ABCDEFGHIJKL'.includes(g))) {
    errors.push(`行${num}: 通過グループが不正 "${groups}"`);
    continue;
  }
  // 割り当ても8文字
  if (assignArr.length !== 8) {
    errors.push(`行${num}: 割り当てが8文字でない "${assign}"`);
    continue;
  }
  // 4) 割り当てが通過グループの順列か（過不足なし・重複なし）
  const sortedGroups = [...groupArr].sort().join('');
  const sortedAssign = [...assignArr].sort().join('');
  if (sortedGroups !== sortedAssign) {
    errors.push(`行${num} [${groups}]: 割り当て"${assign}"が通過グループの順列でない (ソート: ${sortedAssign} ≠ ${sortedGroups})`);
  }
  // 5) プール制約
  for (let i = 0; i < SLOTS.length; i++) {
    const slot = SLOTS[i];
    const g = assignArr[i];
    if (!POOLS[slot].includes(g)) {
      errors.push(`行${num} [${groups}]: スロット1${slot} に 3${g} は不可 (プール ${POOLS[slot].join('')})`);
    }
  }
  // 3) キー重複チェック
  if (seenKeys.has(sortedGroups)) {
    errors.push(`行${num}: 通過グループ集合の重複 "${sortedGroups}"`);
  }
  seenKeys.add(sortedGroups);
  table[sortedGroups] = assign;
}

// 1) 行数 & 全網羅チェック
if (rows.length !== 495) errors.push(`行数が495でない: ${rows.length}`);

// C(12,8) を生成して網羅を確認
function combos(arr, k, start = 0, cur = [], out = []) {
  if (cur.length === k) { out.push(cur.join('')); return out; }
  for (let i = start; i < arr.length; i++) { cur.push(arr[i]); combos(arr, k, i + 1, cur, out); cur.pop(); }
  return out;
}
const allCombos = combos('ABCDEFGHIJKL'.split(''), 8);
for (const c of allCombos) {
  if (!table[c]) errors.push(`未収録の組み合わせ: ${c}`);
}

// ---- 結果 ----
if (errors.length > 0) {
  console.error(`❌ 検証失敗 (${errors.length}件):`);
  for (const e of errors.slice(0, 50)) console.error('  - ' + e);
  if (errors.length > 50) console.error(`  ...他 ${errors.length - 50}件`);
  process.exit(1);
}

console.log(`✅ 検証OK: 495行すべてが プール制約・順列・全網羅 を満たしています。`);

