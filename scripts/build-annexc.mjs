const outPath = resolve(__dirname, '../src/data/thirdPlaceAllocation.ts');
mkdirSync(dirname(outPath), { recursive: true });

const entries = Object.keys(table).sort().map((k) => `  '${k}': '${table[k]}',`).join('\n');

const ts = `// =============================================================================
// AUTO-GENERATED — 手動で編集しないこと。
// scripts/build-annexc.mjs により FIFA WC2026 公式 付属書C から生成。
// =============================================================================

// 3位チームと対戦する1位スロット（公式表の列順）
export const THIRD_PLACE_SLOTS = ['A', 'B', 'D', 'E', 'G', 'I', 'K', 'L'] as const;

// key: 通過した3位の所属グループ8つをアルファベット順に連結した文字列
// value: 各スロット(THIRD_PLACE_SLOTS の順)に入る3位の所属グループ
const ANNEX_C: Record<string, string> = {
${entries}
};

/**
 * 付属書Cに基づき、3位通過8グループを R32 の各1位スロットへ割り当てる。
 * @param qualifiedThirdGroups 3位通過した8グループ（順不同）
 * @returns 例 { A: 'C', B: 'J', D: 'I', E: 'F', G: 'H', I: 'G', K: 'L', L: 'K' }
 *          （1Aスロットには C組の3位、1Bスロットには J組の3位 …）
 *          8グループちょうどで無い／表に無い場合は null
 */
export function allocateThirdPlaceByAnnexC(
  qualifiedThirdGroups: string[]
): Record<string, string> | null {
  if (qualifiedThirdGroups.length !== 8) return null;
  const key = [...qualifiedThirdGroups].sort().join('');
  const assign = ANNEX_C[key];
  if (!assign) return null;
  const result: Record<string, string> = {};
  THIRD_PLACE_SLOTS.forEach((slot, i) => {
    result[slot] = assign[i];
  });
  return result;
}
`;

writeFileSync(outPath, ts, 'utf8');
console.log(`✅ 生成: ${outPath} (${Object.keys(table).length}件)`);
